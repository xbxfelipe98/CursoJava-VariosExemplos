package one.digitalinovationone.whiles;

public class While
{
    public static void main(String[] args)
    {
        var x = 0;

        while (x<1)
        {
            System.out.println("Dentro");
            x++;
        }

        var Y=0;

        do {
                System.out.println("Dentro");

        }while (Y++ < 1);
    }
}
